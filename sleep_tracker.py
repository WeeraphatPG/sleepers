import datetime

def ประเมินคุณภาพการนอน(อายุ, ชั่วโมงการนอน, ระดับกิจกรรม):
    
    if อายุ < 18:
        ชั่วโมงที่แนะนำ = 8
    elif อายุ < 65:
        ชั่วโมงที่แนะนำ = 7
    else:
        ชั่วโมงที่แนะนำ = 7
    
    
    if ระดับกิจกรรม == "หนัก":
        ปรับตามกิจกรรม = -0.5
    elif ระดับกิจกรรม == "กลาง":
        ปรับตามกิจกรรม = 0
    else:  # เบา
        ปรับตามกิจกรรม = 0.5
    
    ชั่วโมงที่แนะนำ += ปรับตามกิจกรรม
    
    
    if ชั่วโมงการนอน >= ชั่วโมงที่แนะนำ:
        return "คุณภาพการนอนของคุณดีแล้ว ควรรักษาไว้!"
    else:
        return "คุณอาจต้องนอนมากขึ้นเพื่อสุขภาพที่ดี"

def main():
    print("แอปติดตามการนอนหลับ")  
    อายุ = int(input("กรอกอายุของคุณ: "))
    เวลาเข้านอน = input("กรอกเวลาเข้านอน (รูปแบบ HH:MM): ")
    เวลาตื่นนอน = input("กรอกเวลาตื่นนอน (รูปแบบ HH:MM): ")
    ระดับกิจกรรม = input("กรอกระดับกิจกรรม (หนัก, กลาง, เบา): ").lower()

    
    เวลาเข้านอน_dt = datetime.datetime.strptime(เวลาเข้านอน, "%H:%M")
    เวลาตื่นนอน_dt = datetime.datetime.strptime(เวลาตื่นนอน, "%H:%M")
    
    
    if เวลาตื่นนอน_dt < เวลาเข้านอน_dt:
        เวลาตื่นนอน_dt += datetime.timedelta(days=1)
    
    ชั่วโมงการนอน = (เวลาตื่นนอน_dt - เวลาเข้านอน_dt).total_seconds() / 3600.0
    
    
    ผลการประเมิน = ประเมินคุณภาพการนอน(อายุ, ชั่วโมงการนอน, ระดับกิจกรรม)
    print(f"คุณนอนทั้งหมด: {ชั่วโมงการนอน:.2f} ชั่วโมง")
    print("ผลการประเมิน:", ผลการประเมิน)

if __name__ == "__main__":
    main()
